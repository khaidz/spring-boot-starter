    public static char removeAccent(char ch) {
        int index = Arrays.binarySearch(SOURCE_CHARACTERS, ch);
        if (index >= 0) {
            ch = DESTINATION_CHARACTERS[index];
        }
        return ch;
    }

    public static String removeAccent(String s) {
        if (s == null) return null;
        StringBuilder sb = new StringBuilder(s);
        for (int i = 0; i < sb.length(); i++) {
            sb.setCharAt(i, removeAccent(sb.charAt(i)));
        }
        return sb.toString();
    }

    public static String toSlug(String s) {
        if (s == null) return null;
        s = removeAccent(s);
        String slug = s.toLowerCase().trim();
        slug = slug.replaceAll("[^a-z0-9\\s-]", "");
        slug = slug.replaceAll("\\s+", "-");
        slug = slug.replaceAll("-+", "-");
        slug = slug.replaceAll("^-+|-+$", "");
        return slug;
    }

    public static String getFileExtension(String filename) {
        return Optional.ofNullable(filename)
                .filter(f -> f.contains("."))
                .map(f -> f.substring(filename.lastIndexOf(".") + 1))
                .orElse("");
    }

    public static boolean isValidExtension(List<String> extensions, String input) {
        if (extensions == null || input == null) return false;
        return extensions.contains(input.toLowerCase());
    }

    public static PageDto getPage(Page<?> page) {
        PageDto pageDto = new PageDto();
        pageDto.setTotalElement(page.getTotalElements());
        pageDto.setTotalPage(page.getTotalPages());
        pageDto.setCurrentPage(page.getPageable().getPageNumber());
        pageDto.setSize(page.getPageable().getPageSize());
        return pageDto;
    }

